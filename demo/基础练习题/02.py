"""
4.购物卡案例: 小明单位发了100元的购物卡,小明到超市买三类洗化用品:洗发水(15元)、香皂(2元)、牙刷(5元)。要
把100元正好花掉,可有哪些购买组合?
"""
money=100
n = money//15
for i in range(n, -1, -1):
    m = (money-i*15)//15
    for j in range(m, -1, -1):
        k = (money-i*15-j*5)//2
        if (money-i*15-j*5)%2==0:
                print('可选择购买组合:\t\t购买洗发水 {} 瓶, 香皂 {} 块, 牙刷 {} 个.'.format(i,j,k))