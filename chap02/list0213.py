# 读取两个整数并对其进行加减乘除运算（其三：使用str函数输出代码） 

a = int(input('整数a：'))
b = int(input('整数b：'))

print('a + b 等于' + str( a + b) + '。') 
print('a - b 等于' + str(a - b)  + '。') 
print('a * b 等于' + str(a * b)  + '。') 
print('a / b 等于' + str( a / b) + '。') 
print('a // b 等于'+ str( a // b)+ '。') 
print('a % b 等于' + str( a % b) + '。') 
print('a ** b 等于'+ str( a ** b)+ '。')