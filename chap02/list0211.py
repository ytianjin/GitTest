# 读取两个整数并 对其进行加减乘除（其一：读取字符串并将其转换为整数） 

s = input('整数a：') 
a = int(s) 
s = input('整数b：') 
b = int(s) 

print('a + b 等于', a + b, '。') 
print('a - b 等于', a - b, '。') 
print('a * b 等于', a * b, '。') 
print('a / b 等于', a / b, '。') 
print('a // b 等于', a // b, '。') 
print('a % b 等于', a % b, '。') 
print('a ** b 等于', a ** b, '。')
